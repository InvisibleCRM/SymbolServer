package io.invisible.symbol.cleanup.model;

/**
 *
 * @author ValeriiL
 */
public class BuildSymbol {
    
    private String id;
    private String path;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the path
     */
    public String getPath() {
        return path;
    }

    /**
     * @param path the path to set
     */
    public void setPath(String path) {
        this.path = path;
    }
}
